//Main filter
function filterProducts() {
    instanceCounter = 0;
    const txtOutput = text.value.toLowerCase();
    const priceOutput = parseInt(number.value);
    const isTool = kind.value.toLowerCase() === "tool";
    const isLower = lower.checked === true;

    //Empty card container
    while (section.firstChild) {
        section.removeChild(section.firstChild);
    }

    //Search element by element
    for (let product of jsonList) {
        //first by product name
        filterByName(product, txtOutput, priceOutput, isTool, isLower);

    }
    //Default limit
}

function filterByName(product, txtOutput, priceOutput, isTool, isLower) {
    let name = product.name.toLowerCase()
    if (name.indexOf(txtOutput) !== -1) {
        if (isTool) {
            if (product.kind === "tool") {
                //then by product price
                filterByPrice(product, priceOutput, isLower);
            }
        } else if (product.kind === "part") {
            //then by product price
            filterByPrice(product, priceOutput, isLower);
        }
    }
}

function filterByPrice(product, priceOutput, isLower) {

    if (priceOutput !== 0 && !isNaN(priceOutput)) {
        if (isLower) {
            //  let lowerPrice = jsonList.filter(product => product.price <= priceOutput);
            if (parseInt(product.price) <= priceOutput) {

                if (instanceCounter < instanceLimit) {
                    drawCard(product);
                }
            }
        } else if (parseInt(product.price) >= priceOutput) {
            if (instanceCounter < instanceLimit) {
                drawCard(product);
            }
        }

    } else {
        if (instanceCounter < instanceLimit) {
            drawCard(product);
        }
    }
}

